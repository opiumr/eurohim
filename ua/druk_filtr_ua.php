<h1><strong>Фільтри стальні емальовані<br>під тиском (ДРУК-фільтри)</strong></h1>
<p>Фільтри стальні емальовані під тиском призначені для фільтрації невибухонебезпечних рідин 1-4 класу небезпеки з ручним вивантаженням осаду.</p>
<p>Друк фільтр – це путч, який має закриту верхню половину. За рахунок конструкції таких фільтрів з'являється можливість формування в них високого тиску, який буде необхідним для забезпечення максимально високої швидкості фільтрації. Нижня частина пристрою є негерметичною, а необхідний тиск формується за рахунок застосування стисненого повітря.</p>
<p>Найбільш поширеним застосування такого обладнання є в тих випадках, коли мова йде про необхідність обробки ефірних, спиртових та інших органічних розчинників, що відрізняються досить низькою температурою кипіння.</p>
<p>Такі апарати мають досить велику фільтруючу поверхню, за рахунок чого вони відрізняються вкрай високою продуктивністю.</p>
