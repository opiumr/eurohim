<h1><strong>Змішувачі з Z-подібними лопастями</strong></h1>
<h2>Змішувачі типу ЗЛ и ЗШ (ТУ 26-01-517-90)</h2>
<div class="img_2_center">
<img src="../images/smesitelz/1.jpg" id="img-h-175" alt="" title="">
<img src="../images/smesitelz/2.jpg" id="img-h-175" alt="" title="">
<img src="../images/smesitelz/3.jpg" id="img-h-175" alt="" title="">
<img src="../images/smesitelz/4.jpg" id="img-h-175" alt="" title="">
