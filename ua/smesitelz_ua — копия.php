<p>Промисловий змішувач, призначений для розминання і перемішування з можливістю підігріву або охолодження різних напівтвердих мас, паст, клею, м'яких пластичних мас. На сьогоднішній день активно застосовується в хімічній, фармацевтичній, косметичній, харчовій, а також безлічі інших областей сучасної промисловості. У сфері тваринництва змішувач є досить затребуваним при використанні на всіляких комбікормових підприємствах, фермах, звірорадгоспах, птахофабриках і всіляких підсобних господарствах, де його застосування здійснюється з метою збагачення комбікормів, досягнення більш високої якості раціонів, а також поліпшення їх загальної біологічної повноцінності.</p>
