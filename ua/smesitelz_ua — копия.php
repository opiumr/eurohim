<p>Двохвалкові змішувачі періодичної дії типу ЗЛ та ЗШ призначені для розминання та перемішування з одночасним підігрівом або охолодженням різноманітних напівтвердих мас, паст, мастик, клею, м'яких кашоподібних мас, м'яких пластичних мас та інших речовин подібної консистенції.</p>
<p>Змішувачі являють собою змішуючу машину, робочими органами якої є два горизонтально розташованих Z-образних лопастних валка, що обертаються в протилежні сторони, з різними швидкостями, причому обертання валків - реверсивне.</p>
<p>Валки обертаються в кориті спеціальної форми, яке з боків закрите торцевими стінками, а зверху - пилонепроникною кришкою.</p>
<p>Валки приводяться в обертання від електродвигуна через еластичну муфту, редуктор і зрівняльну муфту.</p>
<p>Основний продукт завантажується в корпус через люк в кришці або при відкритій кришці, а додаткові компоненти - через технологічні штуцери. У змішувачах типу ЗЛ готовий продукт вивантажується при відкритій кришці після перекидання корпусу на 110&deg; від горизонтальної площини за допомогою гідроциліндра.</p>
<p>У змішувачах типу ЗШ готовий продукт вивантажується при закритій кришці шляхом видавлювання продукту шнеком. Шнек має кожух, який обігрівається та фланцеве з'єднання. До фланцевого з'єднання можливе підключення трубопроводу, фільєри з корозійностійкої сталі або з вуглецевої сталі з облицюванням корозійностійкою сталлю марки 12Х18Н10Т.</p>
<p>Основний привід і привід механізму перекидання або шнекового вивантаження комплектуються електродвигунами єдиної серії АІР (4А) напругою 380В або вибухозахищеними електродвигунами серії АІМ (ВАО) напругою 380В з рівнем вибухозахисту не нижче 1ЕхdІІВТ4.</p>
