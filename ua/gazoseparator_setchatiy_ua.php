<h1><strong>Газосепаратори сітчасті</strong></h1>
<p>Газосепаратори сітчасті призначені для остаточної очистки природного і попутного нафтового газу від рідини (конденсату, інгібіторагідратоутворення, води) в промислових установках підготовки газу до транспортування, а також газо і нафтопереробних заводах.</p>
<p>Конструктивно сепаратори виготовляються двох типів:<br>
&emsp;&emsp;&emsp;&ensp;Тип 1	- циліндричні вертикальні з корпусним фланцевим роз'ємом діаметром 600, 800 мм на умовний тиск від 1,6 до 6,4 МПа (от 16 до 64кгс/см<sup>2</sup>);<br>
&emsp;&emsp;&emsp;&ensp;Тип 2 - циліндричні вертикальні діаметром 1200, 1600, 2000 мм на умовний тиск від 0,6 до 4,0 МПа (от 6 до 40кгс/см<sup>2</sup>).</p>
