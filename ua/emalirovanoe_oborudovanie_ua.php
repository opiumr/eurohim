<p>Емаль - це склоподібна субстанція, отримана в результаті плавлення неорганічних окисло-силікатних сполук. Нанесення її на метал є хімічною реакцією, де при температурі 900&deg;C відбувається механічний зв'язок матеріалів скло - метал. Технологія «безпористої» якості емальованого покриття - це складний технологічний процес, яким володіє ТОВ «ГК Єврохіммаш К.О.» та має власну рецептуру виробництва емалі, завдяки якій покриття служить тривалий час.</p>
<p>Емальоване обладнання широко використовується в хімічній, фармацевтичній, харчовій промисловості, де при процесах застосовуються агресивні середовища органічних і неорганічних кислот, розчинів і солей, лугів і нейтральних продуктів, або ж виробляються особливо чисті продукти.</p>
<p>Емальоване обладнання застосовується не тільки для вирішення проблеми корозії металів, а й щоб уникнути каталітичних процесів і забруднень продукту частинками металу. У фармацевтичній промисловості емаль запобігає зростанню мікрофлори на стінках обладнання, а в полімерній - утворенню зародків для полімеризації.</p>
<p>Емальоване обладнання працює під тиском не нижче 6 кПа (0,06 кгс/см<sup>2</sup>), від залишкового до 1,6 МПа (16 кгс/см<sup>2</sup>) при температурі від мінус 30&deg;C до плюс 300&deg;C. Внутрішні стандарти регламентують товщину емалі 1,2 мм &plusmn; 0,2 мм, що дає чудовий теплообмін між вмістом реактору і сорочкою, а новітні технології емалювання гарантують підвищену міцність і суцільність покриття.</p>
