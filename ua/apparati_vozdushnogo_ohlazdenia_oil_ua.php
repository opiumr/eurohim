<h1><strong>Апарати повітряного охолодження мастила</strong></h1>
<p>Апарати повітряного охолодження мастила типe АВОМ призначені для охолодження мастила низького тиску на компресорних станціях магістральних газопроводів, а також для конденсації і охолодження пароподібних, газоподібних і рідких середовищ, що застосовуються в технологічних процесах нафтогазопереробної промисловості. Апарати експлуатуються на відкритому повітрі в умовах помірного клімату з середньою температурою найбільш холодної п'ятиденки не нижче мінус 40&deg;С.</p>
<p>Апарат складається з однієї теплообмінної секції з камерами, яка обдувається потоком повітря, що нагнітається вентиляторами. Секція розташована горизонтально і монтується на металевій несучій конструкції разом з осьовими вентиляторами, приводами, жалюзями, дифузорами, коробами.</p>
<p>Апарат поставляється одним блоком в повній заводській готовності і не вимагає дозбірки на місці монтажу.</p>
<p>Конструкція апарату дозволяє змінювати інтенсивність охолодження шляхом регулювання подачі повітря, робочої рідини або зупинки вентиляторів.</p>
