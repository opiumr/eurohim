<p>Вертикальный фильтр представляет собой вертикальный цилиндрический корпус с эллиптическими днищами с одной стороны которого находится быстроразъемный концевой затвор.</p>
<p>Фильтр устанавливается вертикально на приварных опорах. Внутри цилиндрического корпуса фильтра установлен съемный фильтрующий элемент многократного использования представляющий собой решетку, установленную в рамную конструкцию, которая обеспечивает определенное положение решетки и направляет на нее поток нефти.</p>
<p>Фильтрующий элемент представляет собой цилиндрический каркас обтянутый сеткой V-образного сечения или перфорированную обечайку.</p>
<p>Выбор фильтра для конкретных условий эксплуатации осуществляет проектная организация, применяющая его в проекте.</p>
