<h1><strong>Фильтры – грязеуловители</strong></h1>
<p>Фильтры – грязеуловители предназначены для защиты приборов и оборудования нефте и продуктопроводов путем очистки перекачиваемой жидкости от механических примесей, посторонних предметов, глины, парафино-смолистых отложений и окалины, образующихся во время ремонта и эксплуатации линейной части магистрального трубопровода.</p>
<p>Фильтры устанавливаются в трубопроводных системах 200 мм, 250 мм, 300 мм, 350 мм, 400 мм, 500 мм, 600 мм, 700 мм, 800 мм, 1000 мм, 1200 мм, работающих на давление от 1,6 до 8,0 МПа (80 кгс/cм<sup>2</sup>). Климатическое исполнение "У", "ХЛ" и "УХЛ".</p>
<p>Сейсмичность района установки фильтров не более 9 баллов по 12-ти бальной шкале.</p>
<p>Фильтр состоит из камеры с быстродействующим концевым затвором, технологическими патрубками и фильтрующим элементом, в виде перфорированной трубы, изготовленной из коррозионно-стойкой стали. Для замены фильтрующего элемента предусматривается лоток или рама-лоток с тяговым механизмом. Штуцера входа и выхода продукта выполняются в виде патрубков для приварки к трубопроводу или заканчиваются фланцевым соединением с поворотной заглушкой.</p>
<p>Фильтры удобны в эксплуатации. Время открытия хомутового затвора не более 2-х минут. Замена фильтрующего элемента 10-15 минут.</p>
<p>По индивидуальному заказу фильтра могут изготавливаться с противоположным входом и выходом продукта.</p>
<p>Дополнительные фильтрующие элементы многократного использования поставляются по заявке заказчика.</p>
