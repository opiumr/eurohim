<h1><strong>Резервуари двостінні</strong></h1>
<p>Резервуари двостінні горизонтальні сталеві призначені для наливу і зберігання рідких продуктів з тиском насичених парів до 0,04 МПа, а також можуть використовуватися як технологічні горизонтальні двостінні ємності.</p>	
<p>Матеріал корпусу двохстінних резервуарів:<br>
Для районів з розрахунковою температурою зовнішнього повітря:</p>
<ul class="list-fa-cso">
<li>до -40&deg;C - Ст3сп-5;</li>
<li>нижче -40&deg;C - 09Г2С-12.</li>
</ul>
<img class="center" src="http://euromash.kiev.ua/images/prod/capacities/rgd_f.jpg" id="img-h-300">

<p>Резервуари двостінні горизонтальні виготовляються з металу товщиною не менше 4 мм для внутрішнього і зовнішнього корпусу. Можливі варіанти товщини корпусів: 4/4, 5/4, 6/4, 8/4, 5/5, 6/5, 8/5, де перша товщина внутрішнього корпусу, а друга товщина зовнішнього корпусу двохстінного горизонтального резервуара.</p>
<p>Резервуари двостінні вертикальні за типом РВС (РВСД, ДР) виготовляються з металу товщиною не менше 5 мм для внутрішнього і зовнішнього корпусу, більш детальну інформацію можна отримати тільки після розробки проекту.</p>
<p>Об'єми резервуарів від 1 до 100 куб.м.</p>
<p>Для установки технологічного устаткування двохстінного резервуара він обладнаний горловиною з плоскою кришкою.</p>


