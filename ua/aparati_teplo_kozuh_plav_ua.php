<p>Кожухотрубчастий теплообмінник в наш час використовується з метою зміни температури робочого середовища, що знаходиться в рідкому або ж в пароподібному вигляді, забезпечуючи при цьому повну безпеку її агрегатного стану. Використання такого обладнання здійснюється в різних технологічних процесах хімічної, нафтохімічної, нафтопереробної і газової промисловості.</p>
<p>Теплообмінники застосовуються найчастіше в тому випадку, якщо присутні значні відносні переміщення труб і кожуха, так як в такому обладнанні одна з решіток не з'єднується з кожухом і може повністю вільно переміщатися уздовж осі при виникненні температурних подовжень. </p>
<p>Кожухотрубчастий теплообмінник може витримувати тиск з боку холодоагенту не більше 1 МПа, а в міжтрубному просторі тиск може досягати вже 2.5 МПа, в чому і полягає їх основна особливість в порівнянні зі стандартними варіантами, що відрізняються меншими показниками максимально допустимого тиску.</p>
<p>Також оригінальність конструкції даного типу кожухотрубчатого теплообмінного апарату полягає ще і в тому, що трубні ствірки, що знаходяться всередині кожуха, нерухомо закріплюються виключно з одного кінця пристрою в той час, як з протилежного боку трубний пучок встановлюється повністю вільно. За рахунок такої оригінальної конструкції повністю виключаються ризики деформації трубних збірок при виникненні різкого перепаду температури того середовища, за рахунок якого і забезпечується теплообмін.</p>
