<h1>Апарати стальні емальовані<br>з механічним змішуючим пристроєм</h1><br>
<p> Призначені для проведення в агресивних рідинах різних хімічних процесів з підігрівом або охолодженням і перемішуванням різних рідких середовищ з динамічною в'язкістю не більше 7Па, з щільністю не більше 2000 кг/м<sup>3</sup>.</p>
<p>Температура середовища від мінус 20 до плюс 250&deg;С.</p>
<p>Робоче середовище в корпусі апарату - нейтральне, пожежонебезпечне, вибухонебезпечне або
токсична рідина, емульсія, газорідинна суміш або суспензія з масовою часткою
твердої фази не більше 30%.</p>
<p>Робоче середовище в сорочці або змійовику - водопровідна вода або оборотна вода, розсіл,
конденсат, насичена водяна пара або високотемпературний органічний теплоносій
з температурою від мінус 30 до плюс 300&deg;С. Ущільнення сальникове або торцеве (для особливо
токсичних середовищ).</p>
<p>Комплектується вентилем з подовженим слідом, яке дозволяє повністю усунути
застійну зону в штуцері нижнього випуску.</p>
<p>Перемішуючий пристрій, а також інші внутрішні пристрої можуть бути
виготовлені з кислотостійких сталей, корозійностійких сталей (12Х18Н10Т,
10Х17Н13М2Т, 10Х17Н13МЗТ).</p>
