<p>Вертикальний фільтр представляє собою вертикальний циліндричний корпус з еліптичними днищами з одної сторони якого знаходиться швидкодіючий кінцевий затвор.</p>
<p>Фільтр встановлюється вертикально на приварних опорах. Всередині циліндричного корпусу фільтру встановлений знімний фільтруючий елемент багаторазового використання, що являє собою решітку, встановлену в рамну конструкцію, яка забезпечує певне положення решітки та направляє на неї потік нафти.</p>
<p>Фільтруючий елемент представляє собою циліндричний каркас, який обтягнутий сіткою V-подібного перерізу або перфоровау обечайку.</p>
<p>Вибір фільтру для конкретних умов експлуатації здійснює проектна організація, яка використовує його в проекті.</p>
