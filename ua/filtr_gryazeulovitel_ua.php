<h1><strong>Фільтри – брудовловлювачі</strong></h1>
<p>Фільтри – брудовловлювачі призначені для захисту приборів та обладнання нафто та продуктопроводів шляхом очищення рідини, що перекачується від механічних домішок, сторонніх предметів, глини, парафіно-смолистих відкладень та окалини, які утворюються під час ремонту та експлуатації лінійної частини магістрального трубопроводу.</p>
<p>Фільтри встановлюються в трубопроводних системах 200 мм, 250 мм, 300 мм, 350 мм, 400 мм, 500 мм, 600 мм, 700 мм, 800 мм, 1000 мм, 1200 мм, працюючих на тиск від 1,6 до 8,0 МПа (80 кгс/cм<sup>2</sup>). Кліматичне виконання "У", "ХЛ" та "УХЛ".</p>
<p>Сейсмічність району встановлення фільтрів не більше 9 балів по 12-ти бальній шкалі.</p>
<p>Фільтр складається із камери з швидкодіючим концевим затвором, технологічними патрубками та фільтруючим елементом, у вигляді перфорованої труби, виготовленої із корозійно-стійкої сталі. Для замени фільтруючого елементу передбачається лоток або рама-лоток з тяговим механізмом. Штуцери входу та выходу продукту виконуються у вигляді патрубків для приварювання до трубопроводу або закінчуються фланцевим з'єднанням із поворотною заглушкою.</p>
<p>Фільтри зручні в експлуатації. Час відкриття хомутового затвору не больше 2-х хвилин. Заміна фільтруючого елементу 10-15 хвилин.</p>
<p>По індивидуальному замовленню фільтри можуть виготовлятися із протилежним входом та виходом продукту.</p>
<p>Додаткові фільтруючі елементи багаторазового використання поставляються по заявці замовника.</p>
